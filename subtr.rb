class Calculator

  def subtract(number1, number2)
  	number1 - number2
  end

  def multiply(number1, number2)
  	number1 * number2
  end

  def divide(number1, number2)
  	number1 / number2
  end

  def squared(number)
  	number * number
  end

  def multiply_then_divide(number1, number2, number3)
  	(number1 * number2) / number3
  end

















end